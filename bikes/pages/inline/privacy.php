                <h3>

                  MOTOCROSS
                </h3>

                <p>

                    Motocross was first evolved in the U.K. from motorcycle trials competitions, such as the Auto-Cycle Clubs's first quarterly trial in 1909 and the Scottish Six Days Trial that began in 1912.
                    When delicate balancing and strict scoring of trials were dispensed with in favour of a race to be the fastest rider to the finish, it was called scrambles, said to have originated in
                    the phrase, 'a rare old scramble' describing one such early race. Originally known as scrambles racing in the United Kingdom, as the sport grew in popularity, the competitions became
                    known internationally as motocross racing, by combining the French word for motorcycle, motocyclette, or moto for short, into a portmanteau with 'cross country'.
                    The first known scramble race took place at Camberley, Surrey in 1924.[3] During the 1930s, the sport grew in popularity, especially in Britain where teams from the Birmingham Small
                    Arms Company (BSA), Norton, Matchless, Rudge, and AJS competed in the events. Off-road bikes from that era differed little from those used on the street. The intense competition over
                    rugged terrain led to technical improvements in motorcycles. Rigid frames gave way to suspensions by the early 1930s, and swinging fork rear suspension appeared by the early 1950s,
                    several years before it was incorporated on the majority of production street bikes. The period after World War
                    II was dominated by BSA which had become the largest motorcycle company in the world. BSA riders dominated international competitions throughout the 1940s
                </p>
