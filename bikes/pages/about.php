           The first internal combustion, petroleum fuele
           d motorcycle was the Daimler Reitwagen. It was designed and built by the German inventors Gottlieb Daimler and Wilhelm
           Maybach in Bad Cannstatt, Germany in 1885. This vehicle was unlike either the safety bicycles or the boneshaker bicycles of
           the era in that it had zero degrees of steering axis angle and no fork offset, and thus did not use the principles of bicycle and
           motorcycle dynamics developed nearly 70 years earlier. Instead, it relied on two outrigger wheels to remain upright while turning
        </p>

