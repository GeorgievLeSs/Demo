            The Butler Petrol Cycle was a three-wheeled vehicle, with the rear wheel directly driven by a 5/8hp (466W) 600
            cc (40 in3; 2¼×5-inch {57×127-mm}) flat twin four stroke engine (with magneto ignition replaced by coil and battery)
            equipped with rotary valves and a float-fed carburettor (five years before Maybach) and Ackermann steering, all of
            which were state of the art at the time. Starting was by compressed air. The engine was liquid-cooled, with a radiator
